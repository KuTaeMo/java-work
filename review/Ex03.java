package review;

public class Ex03 {
	public static void main(String[] args) {
		// 기본 자료형 = primitive type(원시 자료형)
		// 특징 : 사이즈가 정해져 있음.
		// 결과 : 메모리에 해당 공간을 확보할 수 있음.
		// Call By Value = 해당 메모리 공간에 값이 있음.
		
		boolean t1=true;
		byte t8=120; //1Byte (나중에 알아야함)
		short t2=10; //2Byte (몰라도 됨)
		char t3='가'; //2Byte
		int t4=100; //4Byte
		long t5=10000; //8Byte
		float t6=1.5f; //4Byte (몰라도 됨)
		double t7=10.5; // 8Byte
	}
}
